    private void btn_salvaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_salvaMouseClicked
        String nome = txt_nome.getText();
        String casaVin = txt_casaVin.getText();
        String tipo = cmb_tipo.getItemAt(cmb_tipo.getSelectedIndex());        
        String formato = cmb_formato.getItemAt(cmb_formato.getSelectedIndex());
        String annata = String.valueOf(sldr_annata.getValue());
        String zona = txt_regione.getText();
        
        cBottiglia tBott = new cBottiglia(nome,casaVin,tipo,formato,annata,zona);
        
        gest.aggiungi(tBott);
        gest.salva();
        
    }//GEN-LAST:event_btn_salvaMouseClicked

    private void btn_cercaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_cercaMouseClicked
        String cerca = txt_cerca.getText();
        risultatiRicerca.clear();
        pos = 0;
        
        for(int i = 0; i < gest.numElementi(); i++){
            if(cmb_scelta.getSelectedIndex() == 0 && gest.leggi(i).getNome().equals(cerca)){
                risultatiRicerca.add(gest.leggi(i));
            }
            
            if(cmb_scelta.getSelectedIndex() == 1 && gest.leggi(i).getCasaVin().equals(cerca)){
                risultatiRicerca.add(gest.leggi(i));
            }
        }
        if(risultatiRicerca.size() > 0)
            scrivi();
    }//GEN-LAST:event_btn_cercaMouseClicked

    private void btn_indietroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_indietroMouseClicked
        pos --;
        if(pos < 0)
            pos = risultatiRicerca.size() - 1;

        scrivi();
    }//GEN-LAST:event_btn_indietroMouseClicked

    private void btn_avantiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_avantiMouseClicked
        pos ++;
        if(pos >= risultatiRicerca.size())
            pos = 0;
        
        scrivi();
    }//GEN-LAST:event_btn_avantiMouseClicked

    private void btn_eliminaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_eliminaMouseClicked
        if(risultatiRicerca.size() > 0){
            gest.elimina(risultatiRicerca.get(pos));
            risultatiRicerca.remove(pos);
            gest.salva();

        }
        
        if(risultatiRicerca.size() != 0){
            pos = 0;
            scrivi();
        }
    }//GEN-LAST:event_btn_eliminaMouseClicked

